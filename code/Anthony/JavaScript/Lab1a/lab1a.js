let marks = prompt("Please enter your marks", "");
if((90 <= marks) && (marks <= 100) ){
    alert("A grade");
}else if((80 <= marks) && (marks <= 89) ){
    alert("B grade");
}else if((70 <= marks) && (marks <= 79) ){
    alert("C grade");
}else if((60 <= marks) && (marks <= 69) ){
    alert("D grade");
}else if((0 <= marks) && (marks <= 59) ){
    alert("F grade");
}
    